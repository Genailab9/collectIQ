#!/usr/bin/env node
import { readFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const backendRoot = dirname(__dirname);
const repoRoot = dirname(backendRoot);

const sourcePath = join(repoRoot, 'contracts', 'policy.contract.json');
const generatedPolicyPath = join(backendRoot, 'src/policy/generated-policy-contract.ts');
const generatedTargetsPath = join(backendRoot, 'scripts/generated-policy-governance-targets.json');
const generatedBffPath = join(repoRoot, 'frontend/config/bff-contract.json');
const generatedPolicyClientPath = join(repoRoot, 'frontend/config/policy-client-contract.json');

const contract = JSON.parse(readFileSync(sourcePath, 'utf8'));
const expectedPolicyTs = `/* AUTO-GENERATED by scripts/compile-governance-contracts.mjs. DO NOT EDIT. */
export const GENERATED_POLICY_CONTRACT = ${JSON.stringify(contract, null, 2)} as const;
`;

const expectedTargets = JSON.stringify(contract.policyGovernanceTargets ?? [], null, 2);
const expectedBff = JSON.stringify(contract.bff, null, 2);
const expectedPolicyClient = JSON.stringify(
  {
    version: contract.version,
    policyVersion: contract.policyVersion,
    browserApiPrefix: contract.bff?.browserApiPrefix,
    frontendMetricSinkPath: contract.bff?.frontendMetricSinkPath,
    protectedFlags: contract.featureFlags?.protected ?? [],
    traceAccess: contract.routes?.observability?.fullTrace?.access ?? {},
  },
  null,
  2,
);

const violations = [];
if (readFileSync(generatedPolicyPath, 'utf8') !== expectedPolicyTs) {
  violations.push('generated-policy-contract.ts is stale or manually edited');
}
if (readFileSync(generatedTargetsPath, 'utf8').trim() !== expectedTargets.trim()) {
  violations.push('generated-policy-governance-targets.json is stale or manually edited');
}
if (readFileSync(generatedBffPath, 'utf8').trim() !== expectedBff.trim()) {
  violations.push('frontend/config/bff-contract.json is stale or manually edited');
}
if (readFileSync(generatedPolicyClientPath, 'utf8').trim() !== expectedPolicyClient.trim()) {
  violations.push('frontend/config/policy-client-contract.json is stale or manually edited');
}

if (violations.length > 0) {
  console.error('Generated governance artifacts are out of sync:');
  for (const v of violations) {
    console.error(`  - ${v}`);
  }
  console.error('Run: node backend/scripts/compile-governance-contracts.mjs');
  process.exit(1);
}

console.log('check-generated-governance-contracts: OK');
