#!/usr/bin/env node
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const backendRoot = dirname(__dirname);
const repoRoot = dirname(backendRoot);

const sourcePath = join(repoRoot, 'contracts', 'policy.contract.json');
const backendGeneratedPath = join(
  backendRoot,
  'src',
  'policy',
  'generated-policy-contract.ts',
);
const policyTargetsPath = join(backendRoot, 'scripts', 'generated-policy-governance-targets.json');
const frontendBffPath = join(repoRoot, 'frontend', 'config', 'bff-contract.json');
const frontendPolicyClientPath = join(
  repoRoot,
  'frontend',
  'config',
  'policy-client-contract.json',
);

const contract = JSON.parse(readFileSync(sourcePath, 'utf8'));

const generatedTs = `/* AUTO-GENERATED by scripts/compile-governance-contracts.mjs. DO NOT EDIT. */
export const GENERATED_POLICY_CONTRACT = ${JSON.stringify(contract, null, 2)} as const;
`;
writeFileSync(backendGeneratedPath, generatedTs, 'utf8');

writeFileSync(policyTargetsPath, JSON.stringify(contract.policyGovernanceTargets ?? [], null, 2), 'utf8');
writeFileSync(frontendBffPath, JSON.stringify(contract.bff, null, 2), 'utf8');

mkdirSync(dirname(frontendPolicyClientPath), { recursive: true });
writeFileSync(
  frontendPolicyClientPath,
  JSON.stringify(
    {
      version: contract.version,
      policyVersion: contract.policyVersion,
      browserApiPrefix: contract.bff?.browserApiPrefix,
      frontendMetricSinkPath: contract.bff?.frontendMetricSinkPath,
      protectedFlags: contract.featureFlags?.protected ?? [],
      traceAccess: contract.routes?.observability?.fullTrace?.access ?? {},
    },
    null,
    2,
  ),
  'utf8',
);

console.log('compile-governance-contracts: OK');
